package model.other;

public class MySize {
	private float width,hight;

	public MySize(float width, float hight) {
		super();
		this.width = width;
		this.hight = hight;
	}

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}

	public float getHight() {
		return hight;
	}

	public void setHight(float hight) {
		this.hight = hight;
	}
	
}
